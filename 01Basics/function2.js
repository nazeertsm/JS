
//function can store inside the variables

let x = function add(n1, n2) {

    console.log(n1+n2); //10+20 =30 /1020, passing 10 and 20 as numbers
    
}

x(10,20)// instead of call add, we can call x with parameter


//--------------------------------------------------------------------------

//Anonymous function

let t = function (t1, t2) {   //we can remove function name add, since we are not using add

    console.log(t1+t2); //10+20 =30 /1020, passing 10 and 20 as numbers
    
}

t(10,30)// instead of call add, we can call x with parameter

//--------------------------------------------------------------------------

//Arrow function

let a =  (a1, a2)=> {   //we can remove function name add, since we are not using add

    console.log(a1+a2); //10+20 =30 /1020, passing 10 and 20 as numbers
    
}

a(10,40)// instead of call add, we can call x with parameter

